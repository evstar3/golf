#!/bin/sh
set -e

[ "$(./zero "0")" = "" ]
echo 0
[ "$(./zero "0 0")" = "" ]
echo 0 0
[ "$(./zero "1")" = "1" ]
echo 1
[ "$(./zero "0 1")" = "1" ]
echo 0 1
[ "$(./zero "0 1 0")" = "1" ]
echo 0 1 0
[ "$(./zero "0 1 2 3")" = "1 2 3" ]
echo 0 1 2 3
[ "$(./zero "0 1 2 3 0")" = "1 2 3" ]
echo 0 1 2 3 0
[ "$(./zero "0 1 2 3 0 0")" = "1 2 3" ]
echo 0 1 2 3 0 0
[ "$(./zero "0 1 2 0 3 0 0")" = "1 2 0 3" ]
echo 0 1 2 0 3 0 0
